public class Times{

    private String nome;
    private int titulos;

    public Times(String nome) {
        this.nome = nome;
    }

    public void setTitulos(int titulos) {
        this.titulos = titulos;
    }

    public String getNome() {
        return nome;
    }

    public int getTitulos() {
        return titulos;
    }
}